export function defineColorClass(id: number) {
    switch (id) {
        case 1:
            return 'primary'
        case 2:
            return 'secondary'
        case 3:
            return 'success'
        case 4:
            return 'error'
        case 5:
            return 'warning'
        case 6:
            return 'info'

    }
}